def price_of_portfolio(part_one: int, part_two: int) -> int:
    """
    Function is a reusable piece of code, that takes in some inputs, and give an output

    This function takes in two parts, and returns the sum of the two parts
    """
    return part_one + part_two


print(price_of_portfolio(100, 200))
print(price_of_portfolio(200, 200))
print(price_of_portfolio(300, 300))
